#include <cstdio>
int main() {
    int d;
    scanf("%d", &d);
    switch(d) {

        case 0:
            printf("Zero\n");
            break;

        case 1:
            printf("One\n");
            break;

        case 2:
            printf("Two\n");
            break;

        case 3:
            printf("Three\n");
            break;

        case 4:
            printf("Four\n");
            break;

        case 5:
            printf("Five\n");
            break;

        default:
            printf("Greater than 5");

    }








    return 0;
}
